package Model;

public class QuartosModel
{
    private int numeroquarto;
    private String tipoquarto;
    private double precoquarto;
    private String statusquarto;

    public double getPrecoquarto()
    {
        return precoquarto;
    }

    public void setPrecoquarto(double precoquarto)
    {
        this.precoquarto = precoquarto;
    }

    public int getNumeroquarto()
    {
        return numeroquarto;
    }

    public void setNumeroquarto(int numeroquarto)
    {
        this.numeroquarto = numeroquarto;
    }

    public String getTipoquarto()
    {
        return tipoquarto;
    }

    public void setTipoquarto(String tipoquarto)
    {
        this.tipoquarto = tipoquarto;
    }

    public String getStatusquarto()
    {
        return statusquarto;
    }

    public void setStatusquarto(String statusquarto)
    {
        this.statusquarto = statusquarto;
    }
}
